def create_category_keyboard():
    """
    –°–æ–∑–¥–∞–µ—Ç –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É –¥–ª—è –≤—ã–±–æ—Ä–∞ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ –æ–±—Ä–∞—â–µ–Ω–∏—è –ø—Ä–∏ –µ–≥–æ —Å–æ–∑–¥–∞–Ω–∏–∏.
    –ö–∞—Ç–µ–≥–æ—Ä–∏–∏ —Ä–∞—Å–ø–æ–ª–∞–≥–∞—é—Ç—Å—è –ø–æ –¥–≤–µ –≤ —Ä—è–¥, '–î—Ä—É–≥–æ–µ' –∏ '–û—Ç–º–µ–Ω–∞' - –æ—Ç–¥–µ–ª—å–Ω—ã–µ –∫–Ω–æ–ø–∫–∏.
    :return: –û–±—ä–µ–∫—Ç ReplyKeyboardMarkup —Å –∫–Ω–æ–ø–∫–∞–º–∏ –≤—ã–±–æ—Ä–∞ –∫–∞—Ç–µ–≥–æ—Ä–∏–π.
    """
    keyboard = types.ReplyKeyboardMarkup(resize_keyboard=True, row_width=2) # –ö–ª–∞–≤–∏–∞—Ç—É—Ä–∞ —Å –¥–≤—É–º—è –∫–Ω–æ–ø–∫–∞–º–∏ –≤ —Ä—è–¥—É
    categories = list(TICKET_CATEGORIES.keys()) # –ü–æ–ª—É—á–∞–µ–º —Å–ø–∏—Å–æ–∫ –≤—Å–µ—Ö –¥–æ—Å—Ç—É–ø–Ω—ã—Ö –∫–∞—Ç–µ–≥–æ—Ä–∏–π
    # –î–æ–±–∞–≤–ª—è–µ–º –≤—Å–µ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏, –∫—Ä–æ–º–µ 'üÜò –î—Ä—É–≥–æ–µ', –ø–∞—Ä–∞–º–∏
    main_buttons = [types.KeyboardButton(name) for name in categories if name != "üÜò –î—Ä—É–≥–æ–µ"]
    for i in range(0, len(main_buttons), 2):
        if i + 1 < len(main_buttons):
            keyboard.row(main_buttons[i], main_buttons[i + 1])
        else:
            keyboard.row(main_buttons[i])
    # –î–æ–±–∞–≤–ª—è–µ–º 'üÜò –î—Ä—É–≥–æ–µ' –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–æ–π
    keyboard.row(types.KeyboardButton("üÜò –î—Ä—É–≥–æ–µ"))
    # –î–æ–±–∞–≤–ª—è–µ–º '‚ùå –û—Ç–º–µ–Ω–∞' –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–æ–π –¥–ª—è –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç–∏ –ø—Ä–µ—Ä—ã–≤–∞–Ω–∏—è –¥–µ–π—Å—Ç–≤–∏—è
    keyboard.row(types.KeyboardButton("‚ùå –û—Ç–º–µ–Ω–∞"))
    return keyboard

