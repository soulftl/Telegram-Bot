        if not ticket or (ticket['user_id'] != message.from_user.id and message.from_user.id not in ADMIN_IDS):
            bot.send_message(message.chat.id, "–û–±—Ä–∞—â–µ–Ω–∏–µ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ –∏–ª–∏ —É –≤–∞—Å –Ω–µ—Ç –∫ –Ω–µ–º—É –¥–æ—Å—Ç—É–ø–∞.")
